class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
        long long Min_diff = INT_MAX;
        int i = 0 ; 
        int j = m-1;
        sort(a.begin() , a.end());
        while( j < n){
            Min_diff = min(Min_diff , (a[j] - a[i]));
            j++;
            i++;
        }
        return Min_diff;
    //code
    
    }   
};

